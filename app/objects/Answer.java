package objects;

public class Answer {

    String desc;
    int votes;

    public Answer(String desc, int votes) {
        this.desc = desc;
        this.votes = votes;
    }

    public String getDesc() {
        return desc;
    }

    public void setDesc(String desc) {
        this.desc = desc;
    }

    public int getVotes() {
        return votes;
    }

    public void setVotes(int votes) {
        this.votes = votes;
    }

}
